package main

import "fmt"

func main() {
	// **else Expression (Percabangan)
	name := "Budi"

	if name == "Sintya" {
		// Blok if akan dieksekusi ketika kondisi if bernilai true 
		fmt.Println("Hello, Sintya...")
	} else {
		// Blok else akan dieksekusi jika bernilai False
		fmt.Println("Hi, Budi!")
	}

	


}